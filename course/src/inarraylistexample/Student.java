package inarraylistexample;

import java.util.List;

public class Student
{
    private int sid;
    private String sname;
    private List Trainer;
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public List getTrainer() {
		return Trainer;
	}
	public void setTrainer(List trainer) {
		Trainer = trainer;
	}
    
    
}
